                        <p><strong>1. Purpose<a name="1"></a><br>
                          </strong>identiKid is committed to the protection of 
                          personal privacy and as such has adopted a set of privacy 
                          principles.</p>
                        <p>identiKid knows that you care how your personal information is handled and appreciate your trust that we will do so carefully and sensibly and in complying with the Australian National Privacy Principles of the Privacy Act (Private Sector) Amendment 2000.</p>
                        <p><a href="#top" class="type1">Back to top</a></p>
                        <p> <strong>2. Policy</strong><a name="2"></a><br>
                          This policy sets out the principles that identiKid has 
                          adopted in order to protect personal information about 
                          individuals, covering both external individuals and 
                          internal staff. These principles deal with collection, 
                          use and disclosure of personal information as well as 
                          access to information and intrusion issues. These Privacy 
                          Protection Principles are;</p>
                        <p><a href="#top" class="type1">Back to top</a></p>
                        <p><strong>2.1 Collection of Personal 
                          Information</strong><a name="2_1"></a><br>
                          identiKid will only collect personal information that 
                          is necessary for one or more of its legitimate functions 
                          or activities.</p>
                        <p> identiKid will only collect information 
                          by lawful and fair means not in an unreasonably intrusive 
                          way.</p>

                        <p> At or before the time identiKid collects 
                          personal information from the subject of the information 
                          (or, if that is not practical, as soon as practicable 
                          thereafter), identiKid will take reasonable steps to 
                          ensure that the subject of the information is aware 
                          of:</p>
                        <p> (a) identiKid's identity and how 
                          to contact us;<br>
                          <br>
                          <br>
                          (b) the fact that he or she is able to gain access to 
                          the information;<br>
                          <br>

                          <br>
                          (c) the purpose for which the information is collected;<br>
                          <br>
                          <br>
                          (d) to whom (or the types of individuals or organisations 
                          to which) identiKid discloses information of this kind</p>
                        <p> Where it is reasonable and practicable 
                          to do so, identiKid will take reasonable steps to ensure 
                          that the subject of the information is or has been made 
                          aware of the matters listed from (a) to (d) above.</p>
                        <p><a href="#top" class="type1">Back to top</a></p>

                        <p> <strong>2.2 Use </strong><a name="2_2"></a><br>
                          identiKid primarily collects data on customers for the 
                          purpose of contacting them should an order need to be 
                          clarified, or if an alternative method of payment is 
                          needed.</p>
                        <p>Customers will receive our email newsletter 
                          after placing an order, and have the option to be removed 
                          from the mailing list.</p>
                        <p><a href="#top" class="type1">Back to top</a></p>
                        <p> <strong>2.3 Disclosure</strong><a name="2_3"></a><br>

                          identiKid will not disclose personal information of 
                          it's customers.</p>
                        <p><a href="#top" class="type1">Back to top</a></p>
                        <p> <strong>2.4 Data Quality</strong><a name="2_4"></a><br>
                          identiKid will take reasonable steps to make sure the 
                          personal information it collects, uses or discloses 
                          is accurate, complete and up- to- date.</p>
                        <p><a href="#top" class="type1">Back to top</a></p>
                        <p> <strong>2.5 Data Security </strong><a name="2_5" id="2_5"></a><br>

                          identiKid will take reasonable steps to protect the 
                          personal information it holds from misuse and loss and 
                          from unauthorised access, modification or disclosure.<br>
                          identiKid will take reasonable steps to destroy or permanently 
                          de-identify personal information if it is no longer 
                          needed for any purpose.</p>
                        <p><a href="#top" class="type1">Back to top</a></p>
                        <p> <strong>2.6 Openness</strong><a name="2_6" id="2_6"></a><br>
                          Upon request, identiKid will take reasonable steps to 
                          let individuals know, generally, what sort of personal 
                          information it holds, for what purposes, and how it 
                          collects, uses and discloses that information.</p>
                        <p><a href="#top" class="type1">Back to top</a></p>

                        <p> <strong>2.7 Access and Correction</strong><a name="2_7" id="2_7"></a><br>
                          Where identiKid holds personal information about an 
                          individual, it will provide the individual with access 
                          to the information upon written request, in a form or 
                          manner suitable to the individuals reasonable needs, 
                          except to the extent that; providing access would reveal 
                          the intentions of identiKid in relation to negotiations 
                          with the individual in such a way as to prejudice those 
                          negotiations.</p>
                        <p> Where providing access would reveal 
                          evaluative information generated within identiKid in 
                          connection with a commercially sensitive- decision making 
                          process, identiKid may give the individual an explanation 
                          for the decision rather than direct access to the information.</p>
                        <p> If identiKid has given an individual 
                          such an explanation and the individual believes that 
                          direct access to the evaluative information is necessary 
                          to provide a reasonable explanation of the reasons for 
                          the decision, identiKid will, at the request of the 
                          individual, undertake a review of the decision. The 
                          review will be undertaken by personnel other than the 
                          original decision maker.</p>
                        <p> Wherever direct access by the individual 
                          is impractical or inappropriate, identiKid and the individual 
                          should consider whether the use of mutually agreed intermediaries 
                          would allow sufficient access to meet the needs of both 
                          parties.</p>

                        <p> If identiKid holds personal information 
                          about an individual and the individual is able to establish 
                          that the information is not accurate, complete and up- 
                          to- date, identiKid will take reasonable steps to correct 
                          the information so that it is accurate, complete and 
                          up to date.</p>
                        <p> If the individual and identiKid disagree 
                          about whether the information is accurate, complete 
                          and up- to- date, and the individual asks identiKid 
                          to associate with the information a statement claiming 
                          that the information is not accurate, complete or up- 
                          to- date, identiKid will take reasonable steps to do 
                          so.</p>
                        <p> identiKid will provide reasons for 
                          denial of access or correction.</p>
